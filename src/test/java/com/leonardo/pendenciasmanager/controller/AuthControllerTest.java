package com.leonardo.pendenciasmanager.controller;

import com.fasterxml.jackson.databind.ObjectMapper;
import com.leonardo.pendenciasmanager.dto.Request.AuthLoginRequestDTO;
import com.leonardo.pendenciasmanager.dto.Response.AuthResponseDTO;
import com.leonardo.pendenciasmanager.exception.GlobalExceptionHandler;
import com.leonardo.pendenciasmanager.service.AuthService;
import org.junit.jupiter.api.BeforeEach;
import org.junit.jupiter.api.Test;
import org.junit.jupiter.api.extension.ExtendWith;
import org.mockito.InjectMocks;
import org.mockito.Mock;
import org.mockito.junit.jupiter.MockitoExtension;
import org.springframework.http.MediaType;
import org.springframework.test.web.servlet.MockMvc;
import org.springframework.test.web.servlet.setup.MockMvcBuilders;
import org.springframework.validation.beanvalidation.LocalValidatorFactoryBean;

import static org.mockito.ArgumentMatchers.any;
import static org.mockito.Mockito.when;
import static org.springframework.test.web.servlet.request.MockMvcRequestBuilders.post;
import static org.springframework.test.web.servlet.result.MockMvcResultMatchers.jsonPath;
import static org.springframework.test.web.servlet.result.MockMvcResultMatchers.status;

@ExtendWith(MockitoExtension.class)
class AuthControllerTest {

    private MockMvc mockMvc;

    private final ObjectMapper objectMapper = new ObjectMapper();

    @Mock
    private AuthService authService;

    @InjectMocks
    private AuthController authController;

    @BeforeEach
    void setUp() {
        LocalValidatorFactoryBean validator = new LocalValidatorFactoryBean();
        validator.afterPropertiesSet();

        mockMvc = MockMvcBuilders.standaloneSetup(authController)
                .setControllerAdvice(new GlobalExceptionHandler())
                .setValidator(validator)
                .build();
    }

    @Test
    void loginDeveRetornarToken() throws Exception {
        AuthLoginRequestDTO request = new AuthLoginRequestDTO();
        request.setEmail("leo@email.com");
        request.setSenha("123456");

        when(authService.login(any(AuthLoginRequestDTO.class))).thenReturn(new AuthResponseDTO("jwt-token"));

        mockMvc.perform(post("/auth/login")
                        .contentType(MediaType.APPLICATION_JSON)
                        .content(objectMapper.writeValueAsString(request)))
                .andExpect(status().isOk())
                .andExpect(jsonPath("$.token").value("jwt-token"));
    }

    @Test
    void loginDeveRetornarBadRequestQuandoPayloadForInvalido() throws Exception {
        AuthLoginRequestDTO request = new AuthLoginRequestDTO();
        request.setEmail("email-invalido");
        request.setSenha("");

        mockMvc.perform(post("/auth/login")
                        .contentType(MediaType.APPLICATION_JSON)
                        .content(objectMapper.writeValueAsString(request)))
                .andExpect(status().isBadRequest());
    }
}
